/*
Завдання:
Послідовність фібоначчі визначається наступним чином:
    a0=0;
    a1=1;
    ak=ak-1 + ak-2
Для заданого n знайти значення n-го елемента послідовності Фібоначчі (an).
*/
#include <iostream>

int main()
{
    int a=0,b=1,s,n;
    std::cin>>n;
    while (n--)
    {
        s=a+b;
        b=a;
        a=s;
    }
    std::cout<<s;
    return 0;
}
